package org.applab.AppLabMoneyCore.Me2Me;

public class MeToMeGoalType {
    private int goalTypeId = 0;
    private String goalTypeCode = "";
    private String goalTypeName = "";
    private String goalTypeDesc = "";
    
    public int getGoalTypeId() {
        return goalTypeId;
    }
    public void setGoalTypeId(int goalTypeId) {
        this.goalTypeId = goalTypeId;
    }
    public String getGoalTypeCode() {
        return goalTypeCode;
    }
    public void setGoalTypeCode(String goalTypeCode) {
        this.goalTypeCode = goalTypeCode;
    }
    public String getGoalTypeName() {
        return goalTypeName;
    }
    public void setGoalTypeName(String goalTypeName) {
        this.goalTypeName = goalTypeName;
    }
    public String getGoalTypeDesc() {
        return goalTypeDesc;
    }
    public void setGoalTypeDesc(String goalTypeDesc) {
        this.goalTypeDesc = goalTypeDesc;
    }
    
    
}
